public class BikeItem {

    String name;
    String brand;
    String price;
    String milage;
    String style;
    String imageurl;

    public BikeItem(String name, String brand, String price, String milage, String style, String imageurl)
    {
        this.name=name;
        this.brand=brand;
        this.price=price;
        this.milage=milage;
        this.style=style;
        this.imageurl=imageurl;
    }

}
